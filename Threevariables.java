class Threevariables {
    public static void main(String[] args) {
        
        int i1 = 12, i2 = 13, i3 = 14;
        float a1 = 15.11f, a2 = 16.23f, a3 = 17.45f;
        double d1 = 184, d2 = 196, d3 = 20.78;
        long l1 = 1234, l2 = 3456, l3 = 5678;
        char c1 = 'a', c2 = 's', c3 = 'd';
        String s1 = "hellow", s2 = "world", s3 = "good";

        System.out.println("integer value =" + i1);
        System.out.println("integer value =" + i2);
        System.out.println("integer value =" + i3);

        System.out.println("float value =" + a1);
        System.out.println("float value =" + a2);
        System.out.println("float value =" + a3);

        System.out.println("double value =" + d1);
        System.out.println("double value =" + d2);
        System.out.println("double value =" + d3);

        System.out.println("long value =" + l1);
        System.out.println("long value =" + l2);
        System.out.println("long value =" + l3);

        System.out.println("char value =" + c1);
        System.out.println("char value =" + c2);
        System.out.println("char value =" + c3);

        System.out.println("String value =" + s1);
        System.out.println("String value =" + s2);
        System.out.println("String value =" + s3);
    }
}