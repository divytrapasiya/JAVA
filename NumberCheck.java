
class NumberCheck 
{
    public static void main(String[] args) 
    {

        int number=-2;
        System.out.print("Enter a number: ");

        if (number > 0)
        {
            System.out.println("The number is positive.");
        } 
        else if (number < 0) 
        {
            System.out.println("The number is negative.");
        }
         else
        {
            System.out.println("The number is zero.");
        }
    }
}